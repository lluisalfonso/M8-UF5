    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        comp = 0;
        int numcompte = Integer.parseInt(JOptionPane.showInputDialog("Ecriu el numero de compte"));
        int numsecret = Integer.parseInt(JOptionPane.showInputDialog("Ecriu el numero secret del propietari"));
        ite = a.list.iterator();
        while (ite.hasNext()){
            compte = (CCompteBancari) ite.next();
           if(compte.propietari.getnumSecret() == numsecret && compte.getNumCompte() == numcompte){
               JOptionPane.showMessageDialog(this, "Hola " + compte.propietari.getNom() + ", tens " + compte.getSaldo() + " â‚¬ al compte", "Consulta Saldo", JOptionPane.INFORMATION_MESSAGE);
               comp++;
           }
        }
        if(comp == 0){
            JOptionPane.showMessageDialog(this, "Has introduit les dades incorrectes", "Saldo No Consultat", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

